package accessory;

public class encryption 
{
	static { _ini.load(); }

	public static String decrypt(String input_)
	{
		if (!strings.is_ok(input_)) return strings.DEFAULT;

		String output = input_;

		return output;
	}

	public static String encrypt(String input_)
	{
		if (!strings.is_ok(input_)) return strings.DEFAULT;

		String output = input_;

		return output;
	}
}
